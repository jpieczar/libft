#include "libft.h"

void	ft_putendl(const char *s)
{
	if (s != NULL)
	{
		int	i;

		i = 0;
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
