#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	char	*res;

	i = 0;
	if ((n != 0) && !(res = malloc(10)))
	{
		while (n != 0)
		{
			res[i] = (n / 10);
			n /= 10;
			i++;
		}
		res[i] = '\0';
		return (res);
	}
	return (NULL);
}
