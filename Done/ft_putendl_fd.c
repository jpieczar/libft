#include "libft.h"

void	ft_putendl_fd(const char *s, int fd)
{
	if (s != NULL)
	{
		int	i;

		i = 0;
		while (s[i])
		{
			write(fd, &s[i], 1);
			i++;
		}
		write(fd, "\n", 1);
	}
}
